import React, { useEffect, useState } from 'react';
import Head from 'next/head';
import Image from 'next/image';
import BigLogo from '../components/atoms/BigLogo/BigLogo';
import DynamicContainer from '../components/organisms/DynamicContainer/DynamicContainer';
import Navbar from '../components/organisms/Navbar/Navbar';
import styles from '../styles/Home.module.scss';
import { getEvent } from '../stores/reducers/event';
import { useDispatch, useSelector } from 'react-redux';

export default function Home() {
  const dispatch = useDispatch();
  const [openOrderList, setOpenOrderList] = useState(false);

  //TODO fetch current event
  const events = useSelector(state => state.event.data);

  useEffect(() => {
    const status = 'approved';
    dispatch(getEvent(status));
  }, []);

  return (
    <div className={styles.container}>
      <Head>
        <title>SASO Web App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Navbar setOpenOrderList={setOpenOrderList} />
      <BigLogo event={events[0]} />
      {events[0] ? (
        <DynamicContainer
          event={events[0]}
          openOrderList={openOrderList}
          setOpenOrderList={setOpenOrderList}
        />
      ) : (
        ''
      )}
    </div>
  );
}
